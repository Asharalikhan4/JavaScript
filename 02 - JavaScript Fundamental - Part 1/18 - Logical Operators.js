let age = 10;

if(age > 6 && age < 20){
    // execute when both the condition is true
    console.log(age);
} else if (age > 6 || age < 6){
    // execute when at least one of the condition is true
    console.log(age);
} else if(age != 10){
    // execute when condition is false.
    console.log(age);
}